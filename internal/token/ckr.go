// CKRName returns the symbolic name for a CKR_* code, falling back to hex.
func CKRName(code uint) string {
	if n, ok := ckrNames[code]; ok {
		return n
	}
	return fmt.Sprintf("CKR_0x%08X", code)
}

// describeError renders a PKCS#11 error with its symbolic CKR name. Non-PKCS#11
// errors are returned unchanged. This never includes any PIN material.
func describeError(err error) string {
	if err == nil {
		return ""
	}
	var pe pkcs11.Error
	if errors.As(err, &pe) {
		return fmt.Sprintf("%s (0x%08X)", CKRName(uint(pe)), uint(pe))
	}
	return err.Error()
}

// isPINIncorrect reports whether err is CKR_PIN_INCORRECT.
func isPINIncorrect(err error) bool {
	var pe pkcs11.Error
	if errors.As(err, &pe) {
		return uint(pe) == 0x000000A0
	}
	return false
}

// isPINLocked reports whether err is CKR_PIN_LOCKED.
func isPINLocked(err error) bool {
	var pe pkcs11.Error
	if errors.As(err, &pe) {
		return uint(pe) == 0x000000A4
	}
	return false
}
